package org.fundamentals.Practice;

public class FractionalType
{
    public static void main(String[] args) {
        float var1 = .3f;
        float var2 = .2f;
        float var3 = .3f - .2f;
        System.out.println(var3);

        double varA = 3.003;
        double varB = 2.003;
        double varC = varA - varB;
        System.out.println(varC);
    }

}
